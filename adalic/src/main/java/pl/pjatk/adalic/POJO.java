package pl.pjatk.adalic;

public class POJO {
    private String s;
    private int i;
    private int  n;
    private int t;

    public POJO(String s, int i, int n, int t) {
        this.s = s;
        this.i = i;
        this.n = n;
        this.t = t;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }
}
